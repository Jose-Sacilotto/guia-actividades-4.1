precio2=float(input("Precio del producto: "))
cantidad2=int(input("Ingresa la cantidad de productos: "))
producto3=input("Ingresa el nombre de el producto: ")
precio3=float(input("Precio del producto: "))
cantidad3=int(input("Ingresa la cantidad de productos: "))
